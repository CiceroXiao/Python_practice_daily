user_input = input("Enter the message to display with the bitmap.\n â†’ ")

if (user_input == "") or (not user_input.strip()):
    sys.exit()

user_string_to_list = list(user_input)

for line in bitmap.splitlines():
    for i, letter in enumerate(line):
        if letter == "*":
            bitmap = bitmap.replace(
              letter, user_string_to_list[i % len(user_string_to_list)], 1)

print(bitmap)
print()
